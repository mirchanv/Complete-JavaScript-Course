// 03 Operators -  Comparison operators

const num1 = 5;
const num2 = 10;
const num3 = 15;

// ==, ===
console.log("5 == 5  :", num1 == 5);
console.log("5 === 5 :", num1 === 5);
console.log("5 === 5 :", num1 === "5");

// !=, !==
console.log("5 != 3  :", num1 != 3);
console.log("5 !== 3 :", num1 !== "5");

// >, <, >=, <=
console.log("5 > 10  :", num1 > num2);
console.log("10 < 15 :", num2 < num3);
console.log("5 >= 5  :", num1 >= 5);
console.log("5 <= 4  :", num1 <= 4);
